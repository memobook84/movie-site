export default async function SelectionPage() {
  const genreData = await Promise.all(
    GENRES.map(async (genre) => {
      const movies = await Promise.all(
        genre.picks.map(async (pick) => {
          const detail = await getMovieDetail(pick.id);
          return {
            detail: {
              id: detail.id,
              title: detail.title,
              name: detail.name,
              poster_path: detail.poster_path,
              release_date: detail.release_date,
            },
            review: pick.review,
          };
        })
      );
      return { label: genre.label, movies };
    })
  );

  return (
    <main className="min-h-screen bg-white pt-14 md:pt-24 pb-28">
      <div className="mx-auto max-w-[1280px] px-6 md:px-16">
        <h1 className="text-lg font-normal tracking-tight text-gray-900 font-[family-name:var(--font-noto-sans-jp)] md:text-3xl">
          おすすめセレクション
        </h1>
        <p className="mt-1 text-xs text-gray-400 md:text-sm">
          ジャンル別に厳選した名作を一言レビューとともに
        </p>

        <SelectionClient genres={genreData} />
      </div>
    </main>
  );
}
